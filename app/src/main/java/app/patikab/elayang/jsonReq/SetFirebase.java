public class SetFirebase {

    public String firebase_device;
    public String firebase_token;
    public String firebase_info;

    public SetFirebase() {
    }

    public String getFirebase_device() {
        return firebase_device;
    }

    public void setFirebase_device(String firebase_device) {
        this.firebase_device = firebase_device;
    }

    public String getFirebase_token() {
        return firebase_token;
    }

    public void setFirebase_token(String firebase_token) {
        this.firebase_token = firebase_token;
    }

    public String getFirebase_info() {
        return firebase_info;
    }

    public void setFirebase_info(String firebase_info) {
        this.firebase_info = firebase_info;
    }
}
